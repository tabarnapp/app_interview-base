import { ShopifyAnalytics } from "./Shopify.js";

export const formatMoney = function(amount) {


  if (ShopifyAnalytics && ShopifyAnalytics.meta && ShopifyAnalytics.meta.currency && syms[ShopifyAnalytics.meta.currency]) {
    const symb_data = syms[ShopifyAnalytics.meta.currency];
    let currency = (amount / 100).toFixed(2);
    if (symb_data.onleft === false) {
      currency = currency + symb_data.sym;
    } else {
      currency = symb_data.sym + currency;
    }
    return currency;
  };

  if (Shopify && Shopify.formatMoney) {
    try {
      const amount_formatted = Shopify.formatMoney(amount);
      return amount_formatted;
    } catch (e) {}
  };

  return (amount / 100).toFixed(2);
};